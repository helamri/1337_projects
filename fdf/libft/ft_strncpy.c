#include "libft.h"

char	*ft_strncpy(char *dest, char const *src, size_t n)
{
	size_t i;

	i = 0;
	while (src[i] && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	if (n > ft_strlen(src))
	{
		while (i < n)
			dest[i++] = '\0';
	}
	return (dest);
}
