#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	size_t a;

	a = 0;
	if (!s1 || !s2)
		return (0);
	while (s1[a] || s2[a])
	{
		if (s1[a] != s2[a])
			return (0);
		a++;
	}
	return (1);
}
