#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*c;
	size_t	i;

	i = 0;
	if (!(c = ft_memalloc(size + 1)))
		return (NULL);
	while (i < size)
	{
		c[i] = '\0';
		++i;
	}
	return (c);
}
